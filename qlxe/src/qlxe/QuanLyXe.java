    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = Connect.openConnection();
            String sql = "INSERT INTO thongtin(TenKhach,SDT,LoaiXe,BienSo) VALUES(?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jComboBox1.getSelectedItem().toString());
            pst.setString(4, jTextField3.getText());
            if(pst.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Gửi xe thành công!");
                JOptionPane.showMessageDialog(this, "Vé xe của bạn là: " + max());
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                tbn.setRowCount(0);
                loadData();
                loadSoxe();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

