    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ten = "", sdt = "", loai = "", bienso = "";
        try {
            Connection conn = Connect.openConnection();
            PreparedStatement st = conn.prepareStatement("select * from thongtin where id=?");
            String ve = timVexe.getText();
            StringBuilder sb = new StringBuilder();
            if (ve.equals("")) {
                sb.append("Nhap ve xe!\n");
            }
            if (sb.length() > 0) {
                JOptionPane.showMessageDialog(this, sb.toString(), "Invalidation", JOptionPane.ERROR_MESSAGE);
                return;
            }
            st.setString(1,ve);
            ResultSet rs = st.executeQuery();
            int gia=0;
            
            if (rs.next()) {
                ten=rs.getString(2);
                sdt=rs.getString(3);
                loai=rs.getString(4);
                bienso=rs.getString(5);
                if(loai.equals("Xe Đạp")) {
                    gia = 3000;
                } else if (loai.equals("Xe Điện")) {
                    gia = 5000;
                } else if (loai.equals("Xe Máy")) {
                    gia = 10000;
                } else if (loai.equals("Ô Tô")) {
                    gia = 30000;
                } else {
                    gia = 15000;
                }
            }
            String sql = "Delete from thongtin where id=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, timVexe.getText());
            if(JOptionPane.showConfirmDialog(this, "Bạn cần phải thanh toán " + gia +"d!", "Confirm",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                PreparedStatement t = conn.prepareStatement("INSERT INTO lichsu(TenKhach,SDT,LoaiXe,BienSo,GiaTien,ThoiGian) VALUES(?,?,?,?,?,?)");
                    t.setString(1, ten);
                    t.setString(2, sdt);
                    t.setString(3, loai);
                    t.setString(4, bienso);
                    t.setString(5, String.valueOf(gia));
                    Date date = new Date();
                    SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss dd/MM/YYYY");
                    t.setString(6, sdf.format(date.getTime()));
                    t.executeUpdate();
                
                
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Thanh toán thành công!");
            timVexe.setText("");
            tbn.setRowCount(0);
            loadData();
            loadLichsu();
            loadTongthu();
        }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

